void	ft_count_words(unsigned int *w, char *s, char c)
{
	int		i;

	i = 0;
	*w = 0;
	while (s[i])
	{
		while ((s[i] == c) && (s[i] != '\0'))
			i++;
		if (s[i] != '\0')
			*w += 1;
		while ((s[i] != c) && (s[i] != '\0'))
			i++;
	}
}
